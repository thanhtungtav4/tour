// Helper to generate future dates
const getFutureDates = (startOffset: number, count: number) => {
  const dates = [];
  const today = new Date();
  for (let i = 0; i < count; i++) {
    const date = new Date(today);
    date.setDate(today.getDate() + startOffset + (i * 7)); // Every 7 days
    dates.push({
      date: date.toISOString().split("T")[0],
      availableSpots: Math.floor(Math.random() * 15) + 5,
    });
  }
  return dates;
};

